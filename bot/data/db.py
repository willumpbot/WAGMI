# ─── Performance Aggregation ──────────────────────────────────────

def update_daily_performance(date: str = ""):
    """Aggregate and store daily performance stats. Idempotent."""
    if not date:
        date = datetime.now(timezone.utc).strftime("%Y-%m-%d")
    conn = None
    try:
        conn = get_connection()
        rows = conn.execute(
            "SELECT * FROM trades WHERE timestamp >= ? AND timestamp < ? AND action != 'OPEN'",
            (date, date + "T23:59:59Z")
        ).fetchall()

        if not rows:
            return

        trades = [dict(r) for r in rows]
        closes = [t for t in trades if t["action"] in CLOSE_ACTIONS]
        if not closes:
            return

        # Aggregate overall
        _upsert_perf(conn, date, "ALL", "ALL", closes)

        # By strategy
        strats: Dict[str, list] = {}
        for t in closes:
            s = t.get("strategy", "unknown")
            strats.setdefault(s, []).append(t)
        for s, strades in strats.items():
            _upsert_perf(conn, date, "ALL", s, strades)

        # By symbol
        syms: Dict[str, list] = {}
        for t in closes:
            syms.setdefault(t["symbol"], []).append(t)
        for sym, strades in syms.items():
            _upsert_perf(conn, date, sym, "ALL", strades)

        conn.commit()
    except Exception as e:
        logger.warning(f"Failed to update daily performance: {e}")
        if conn:
            try:
                conn.rollback()
            except Exception:
                pass
    finally:
        if conn:
            conn.close()


def _upsert_perf(conn, date: str, symbol: str, strategy: str,
                 closes: List[Dict]):
    """Insert or update a performance_daily row."""
    wins = [t for t in closes if t["pnl"] > 0]
    gross = sum(t["pnl"] for t in closes)
    fees = sum(t["fee"] for t in closes)
    pnls = [t["pnl"] for t in closes]

    conn.execute(
        """INSERT INTO performance_daily
           (date, symbol, strategy, trades, wins, losses,
            gross_pnl, net_pnl, total_fees,
            best_trade_pnl, worst_trade_pnl, avg_leverage)
           VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
           ON CONFLICT(date, symbol, strategy) DO UPDATE SET
           trades=?, wins=?, losses=?,
           gross_pnl=?, net_pnl=?, total_fees=?,
           best_trade_pnl=?, worst_trade_pnl=?, avg_leverage=?""",
        (date, symbol, strategy,
         len(closes), len(wins), len(closes) - len(wins),
         gross, gross - fees, fees,
         max(pnls) if pnls else 0, min(pnls) if pnls else 0,
         sum(t.get("leverage", 1) for t in closes) / len(closes),
         # UPDATE values
         len(closes), len(wins), len(closes) - len(wins),
         gross, gross - fees, fees,
         max(pnls) if pnls else 0, min(pnls) if pnls else 0,
         sum(t.get("leverage", 1) for t in closes) / len(closes),
         )
    )


def get_performance_history(days: int = 30) -> List[Dict]:
    """Get daily performance history for the last N days."""
    cutoff = (datetime.now(timezone.utc) - timedelta(days=days)).strftime("%Y-%m-%d")
    try:
        conn = get_connection()
        rows = conn.execute(
            """SELECT * FROM performance_daily
               WHERE date >= ? AND symbol = 'ALL' AND strategy = 'ALL'
               ORDER BY date""",
            (cutoff,)
        ).fetchall()
        conn.close()
        return [dict(r) for r in rows]
    except Exception:
        return []


