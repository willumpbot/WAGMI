"""
SQLite persistence layer for trade journal, signals, equity tracking,
signal outcome scoring, and health monitoring.

Crash-proof storage that survives restarts and provides data for analysis.

Tables:
- signals: every signal generated by strategies (even non-traded ones)
- trades: every position open/close/TP/SL event
- equity_snapshots: periodic equity + position state for curve tracking
- signal_outcomes: links signals to their trade results for performance tracking
- health_events: bot health anomalies (stalls, errors, restarts)
"""

import sqlite3
import json
import logging
import time
from datetime import datetime, timezone, timedelta
from pathlib import Path
from typing import Dict, Any, List, Optional, Tuple

logger = logging.getLogger("bot.db")

DB_PATH = Path("ml_data") / "bot.db"


def get_connection() -> sqlite3.Connection:
    """Get a thread-local SQLite connection."""
    conn = sqlite3.connect(str(DB_PATH), timeout=10)
    conn.row_factory = sqlite3.Row
    conn.execute("PRAGMA journal_mode=WAL")  # better concurrent reads
    return conn


def init_db():
    """Create tables if they don't exist."""
    DB_PATH.parent.mkdir(parents=True, exist_ok=True)
    conn = get_connection()
    conn.executescript("""
        CREATE TABLE IF NOT EXISTS signals (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            timestamp TEXT NOT NULL,
            symbol TEXT NOT NULL,
            strategy TEXT NOT NULL,
            side TEXT NOT NULL,
            confidence REAL NOT NULL,
            entry REAL,
            sl REAL,
            tp1 REAL,
            tp2 REAL,
            atr REAL,
            leverage REAL DEFAULT 1.0,
            traded INTEGER DEFAULT 0,
            metadata TEXT
        );

        CREATE TABLE IF NOT EXISTS trades (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            timestamp TEXT NOT NULL,
            symbol TEXT NOT NULL,
            action TEXT NOT NULL,
            side TEXT NOT NULL,
            price REAL NOT NULL,
            qty REAL,
            pnl REAL DEFAULT 0.0,
            fee REAL DEFAULT 0.0,
            leverage REAL DEFAULT 1.0,
            strategy TEXT,
            metadata TEXT
        );

        CREATE TABLE IF NOT EXISTS equity_snapshots (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            timestamp TEXT NOT NULL,
            equity REAL NOT NULL,
            open_positions INTEGER DEFAULT 0,
            daily_pnl REAL DEFAULT 0.0,
            unrealized_pnl REAL DEFAULT 0.0
        );

        CREATE TABLE IF NOT EXISTS signal_outcomes (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            signal_id INTEGER NOT NULL,
            symbol TEXT NOT NULL,
            strategy TEXT NOT NULL,
            side TEXT NOT NULL,
            confidence REAL NOT NULL,
            entry_price REAL,
            exit_price REAL,
            exit_action TEXT,
            pnl REAL DEFAULT 0.0,
            hold_time_s REAL DEFAULT 0.0,
            outcome TEXT,
            scored_at TEXT NOT NULL,
            FOREIGN KEY (signal_id) REFERENCES signals(id)
        );

        CREATE TABLE IF NOT EXISTS health_events (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            timestamp TEXT NOT NULL,
            event_type TEXT NOT NULL,
            severity TEXT NOT NULL DEFAULT 'warning',
            message TEXT,
            details TEXT
        );

        CREATE INDEX IF NOT EXISTS idx_signals_symbol ON signals(symbol);
        CREATE INDEX IF NOT EXISTS idx_signals_ts ON signals(timestamp);
        CREATE INDEX IF NOT EXISTS idx_trades_symbol ON trades(symbol);
        CREATE INDEX IF NOT EXISTS idx_trades_ts ON trades(timestamp);
        CREATE INDEX IF NOT EXISTS idx_trades_sym_action_ts ON trades(symbol, action, timestamp);
        CREATE INDEX IF NOT EXISTS idx_equity_ts ON equity_snapshots(timestamp);
        CREATE INDEX IF NOT EXISTS idx_signal_outcomes_symbol ON signal_outcomes(symbol);
        CREATE INDEX IF NOT EXISTS idx_signal_outcomes_strategy ON signal_outcomes(strategy);
        CREATE INDEX IF NOT EXISTS idx_signal_outcomes_scored ON signal_outcomes(scored_at);
        CREATE INDEX IF NOT EXISTS idx_health_ts ON health_events(timestamp);
        CREATE INDEX IF NOT EXISTS idx_health_type ON health_events(event_type);
    """)
    conn.close()
    logger.info(f"Database initialized: {DB_PATH}")


# ─── Write helpers ────────────────────────────────────────────────

def log_signal(symbol: str, strategy: str, side: str, confidence: float,
               entry: float = 0, sl: float = 0, tp1: float = 0, tp2: float = 0,
               atr: float = 0, leverage: float = 1.0, traded: bool = False,
               metadata: Optional[Dict] = None) -> Optional[int]:
    """Log a generated signal. Returns the signal row ID (or None on failure)."""
    conn = None
    try:
        conn = get_connection()
        cursor = conn.execute(
            """INSERT INTO signals (timestamp, symbol, strategy, side, confidence,
               entry, sl, tp1, tp2, atr, leverage, traded, metadata)
               VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)""",
            (datetime.now(timezone.utc).isoformat(), symbol, strategy, side,
             confidence, entry, sl, tp1, tp2, atr, leverage, int(traded),
             json.dumps(metadata) if metadata else None)
        )
        conn.commit()
        return cursor.lastrowid
    except Exception as e:
        logger.warning(f"Failed to log signal: {e}")
        if conn:
            try:
                conn.rollback()
            except Exception:
                pass
        return None
    finally:
        if conn:
            conn.close()


