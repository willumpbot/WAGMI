# ─── Strategy Performance (aggregated from signal outcomes) ──────

def get_strategy_leaderboard(days: int = 7) -> List[Dict]:
    """Get strategy ranking by profitability for the last N days."""
    cutoff = (datetime.now(timezone.utc) - timedelta(days=days)).isoformat()
    try:
        conn = get_connection()
        rows = conn.execute(
            """SELECT strategy,
                      COUNT(*) as trades,
                      SUM(CASE WHEN outcome = 'WIN' THEN 1 ELSE 0 END) as wins,
                      SUM(pnl) as total_pnl,
                      AVG(pnl) as avg_pnl,
                      AVG(hold_time_s) as avg_hold_s
               FROM signal_outcomes
               WHERE scored_at >= ?
               GROUP BY strategy
               ORDER BY total_pnl DESC""",
            (cutoff,)
        ).fetchall()
        conn.close()
        return [dict(r) for r in rows]
    except Exception:
        return []
