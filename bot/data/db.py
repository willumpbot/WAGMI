# All actions that represent a position close (for consistent filtering)
CLOSE_ACTIONS = {"SL", "TP1", "TP2", "TRAILING_STOP", "EARLY_EXIT", "EMERGENCY",
                 "ROTATE_OUT", "ROTATE_LOSS", "MANUAL_CLOSE"}


def get_daily_summary() -> Dict[str, Any]:
    """Generate daily performance summary."""
    trades = get_trades_today()
    closes = [t for t in trades if t["action"] in CLOSE_ACTIONS]

    if not closes:
        return {"total_trades": 0, "net_pnl": 0, "win_rate": 0}

    wins = [t for t in closes if t["pnl"] > 0]
    total_pnl = sum(t["pnl"] for t in closes)
    total_fees = sum(t["fee"] for t in closes)

    # By strategy
    by_strategy = {}
    for t in closes:
        s = t.get("strategy", "unknown")
        if s not in by_strategy:
            by_strategy[s] = {"trades": 0, "wins": 0, "pnl": 0.0}
        by_strategy[s]["trades"] += 1
        if t["pnl"] > 0:
            by_strategy[s]["wins"] += 1
        by_strategy[s]["pnl"] += t["pnl"]

    # Best/worst strategy
    best = max(by_strategy.items(), key=lambda x: x[1]["pnl"]) if by_strategy else ("none", {})
    worst = min(by_strategy.items(), key=lambda x: x[1]["pnl"]) if by_strategy else ("none", {})

    return {
        "total_trades": len(closes),
        "wins": len(wins),
        "losses": len(closes) - len(wins),
        "win_rate": len(wins) / len(closes) if closes else 0,
        "net_pnl": total_pnl - total_fees,
        "total_fees": total_fees,
        "by_strategy": by_strategy,
        "best_strategy": best[0],
        "worst_strategy": worst[0],
    }


# ─── Signal Outcome Tracking ─────────────────────────────────────

def log_signal_outcome(
    symbol: str, strategy: str, side: str, confidence: float,
    entry_price: float = 0, exit_price: float = 0,
    exit_action: str = "", pnl: float = 0, pnl_pct: float = 0,
    hold_time_s: float = 0, regime: str = "", leverage: float = 1.0,
    win: bool = False, signal_id: Optional[int] = None,
) -> Optional[int]:
    """Record a signal's outcome for performance tracking."""
    # Compute a quality score: weighted combination of factors
    score = _compute_signal_score(pnl_pct, confidence, hold_time_s, win)
    conn = None
    try:
        conn = get_connection()
        cursor = conn.execute(
            """INSERT INTO signal_outcomes
               (signal_id, timestamp, symbol, strategy, side, confidence,
                entry_price, exit_price, exit_action, pnl, pnl_pct,
                hold_time_s, regime, leverage, win, score)
               VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)""",
            (signal_id, datetime.now(timezone.utc).isoformat(),
             symbol, strategy, side, confidence,
             entry_price, exit_price, exit_action, pnl, pnl_pct,
             hold_time_s, regime, leverage, int(win), score)
        )
        conn.commit()
        return cursor.lastrowid
    except Exception as e:
        logger.warning(f"Failed to log signal outcome: {e}")
        if conn:
            try:
                conn.rollback()
            except Exception:
                pass
        return None
    finally:
        if conn:
            conn.close()


def _compute_signal_score(pnl_pct: float, confidence: float,
                          hold_time_s: float, win: bool) -> float:
    """Compute a quality score for a signal outcome (0-100)."""
    score = 50.0  # baseline
    # PnL contribution (capped at +/- 30 points)
    pnl_contrib = min(max(pnl_pct * 5, -30), 30)
    score += pnl_contrib
    # Confidence accuracy bonus: high confidence + win = good
    if win and confidence >= 75:
        score += 10
    elif not win and confidence >= 75:
        score -= 10  # overconfident loss
    # Hold time penalty for very long holds (> 24h)
    if hold_time_s > 86400:
        score -= 5
    return max(0, min(100, score))


def get_signal_performance(days: int = 7, symbol: str = "",
                           strategy: str = "") -> Dict[str, Any]:
    """Get signal performance stats over a period."""
    cutoff = (datetime.now(timezone.utc) - timedelta(days=days)).isoformat()
    conn = None
    try:
        conn = get_connection()
        query = "SELECT * FROM signal_outcomes WHERE timestamp >= ?"
        params: list = [cutoff]
        if symbol:
            query += " AND symbol = ?"
            params.append(symbol)
        if strategy:
            query += " AND strategy = ?"
            params.append(strategy)
        rows = conn.execute(query, params).fetchall()
        conn.close()

        if not rows:
            return {"total": 0, "win_rate": 0, "avg_pnl": 0, "avg_score": 0}

        outcomes = [dict(r) for r in rows]
        wins = [o for o in outcomes if o["win"]]
        total_pnl = sum(o["pnl"] for o in outcomes)
        avg_score = sum(o["score"] for o in outcomes) / len(outcomes)
        avg_conf = sum(o["confidence"] for o in outcomes) / len(outcomes)
        avg_hold = sum(o["hold_time_s"] for o in outcomes) / len(outcomes)

        # By strategy breakdown
        by_strat: Dict[str, Dict] = {}
        for o in outcomes:
            s = o["strategy"]
            if s not in by_strat:
                by_strat[s] = {"trades": 0, "wins": 0, "pnl": 0.0, "scores": []}
            by_strat[s]["trades"] += 1
            if o["win"]:
                by_strat[s]["wins"] += 1
            by_strat[s]["pnl"] += o["pnl"]
            by_strat[s]["scores"].append(o["score"])

        for s in by_strat:
            scores = by_strat[s].pop("scores")
            by_strat[s]["avg_score"] = sum(scores) / len(scores) if scores else 0
            by_strat[s]["win_rate"] = (
                by_strat[s]["wins"] / by_strat[s]["trades"]
                if by_strat[s]["trades"] > 0 else 0
            )

        # By symbol breakdown
        by_sym: Dict[str, Dict] = {}
        for o in outcomes:
            sym = o["symbol"]
            if sym not in by_sym:
                by_sym[sym] = {"trades": 0, "wins": 0, "pnl": 0.0}
            by_sym[sym]["trades"] += 1
            if o["win"]:
                by_sym[sym]["wins"] += 1
            by_sym[sym]["pnl"] += o["pnl"]
        for sym in by_sym:
            by_sym[sym]["win_rate"] = (
                by_sym[sym]["wins"] / by_sym[sym]["trades"]
                if by_sym[sym]["trades"] > 0 else 0
            )

        return {
            "total": len(outcomes),
            "wins": len(wins),
            "losses": len(outcomes) - len(wins),
            "win_rate": len(wins) / len(outcomes),
            "total_pnl": total_pnl,
            "avg_pnl": total_pnl / len(outcomes),
            "avg_score": avg_score,
            "avg_confidence": avg_conf,
            "avg_hold_time_s": avg_hold,
            "by_strategy": by_strat,
            "by_symbol": by_sym,
        }
    except Exception as e:
        logger.warning(f"Failed to get signal performance: {e}")
        return {"total": 0, "win_rate": 0, "avg_pnl": 0, "avg_score": 0}
    finally:
        if conn:
            conn.close()


